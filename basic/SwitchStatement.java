package basic;

public class SwitchStatement {

	public static void main(String[] args) {
	int a=4;
	switch(a){
	case 1: System.out.println("I am @ home");
			break;
	case 2: System.out.println("I am @ College");
			break;
	case 3: System.out.println("I am @ School");
			break;
    default: System.out.println("I am @ work");
	}
	
	char ch='b';
	switch(ch){
	case 'a' :System.out.println("Print a");
			break;
	case 'b' :System.out.println("Print b");
			break;
	
	}
}
}
	
	
